#include <stdio.h>
int main()
{
    char op = '-';
    int a = 10, b = 5;
    if (op = '+')
        printf("Sum: %d\n", a + b);
    else if (op == '-')
        printf("Difference: %d\n", a - b);
    else if (op == '*')
        printf("Product: %d\n", a * b);
    else if (op == '/')
        printf("Quotient: %d\n", a / b);
    else
        printf("Invalid Operator\n");
    return 0;
}
